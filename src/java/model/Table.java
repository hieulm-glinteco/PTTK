package model;

/**
 *
 * @author TT
 */
public class Table {
    private int id;
    private String name, location, note;

    public Table() {
    }

    public Table(int id, String name, String location, String note) {
        this.id = id;
        this.name = name;
        this.location = location;
        this.note = note;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }
    
    
}
