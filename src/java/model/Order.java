package model;

import java.sql.Date;
/**
 *
 * @author TT
 */
public class Order {
    private int id;
    private Date date;
    private String status;

    public Order() {
    }

    public Order(int id, Date date, String status) {
        this.id = id;
        this.date = date;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
